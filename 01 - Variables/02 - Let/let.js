// The 'let' keyword allows variable reassignment but restricts redeclaration within the same scope.

// Declaring a 'let' variable
let studentName = "Justin";
console.log("Student Name:", studentName); // Output: Student Name: Justin

// Reassigning a 'let' variable (Allowed)
studentName = "Michael";
console.log("Updated Student Name:", studentName); // Output: Updated Student Name: Michael