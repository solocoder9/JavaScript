// Declaring a constant variable for student name, which cannot be reassigned
const studentName = "Justin";

// Declaring a variable using 'let' for student email, which can be reassigned
let studentEmail = "Justin15@gmail.com";

// Declaring a variable using 'var' for student roll number, which can be reassigned 
// (but 'var' is less commonly used in modern JavaScript)
var studentRollNo = "23CSE229";

// Outputting the values of the variables to the console
console.log(studentName); // Prints the student's name
console.log(studentEmail); // Prints the student's email
console.log(studentRollNo); // Prints the student's roll number
