// Example 3: Checking traffic light color
let trafficLight = "yellow";

switch (trafficLight) {
    case "red":
        console.log("Stop!");
        break;
    case "yellow":
        console.log("Slow down!"); // Output: Slow down!
        break;
    case "green":
        console.log("Go!");
        break;
    default:
        console.log("Invalid traffic light color.");
}