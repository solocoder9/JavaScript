// Example 2: Checking age group
let age = 25;

if (age < 13) {
    console.log("You are a child.");
} else if (age < 20) {
    console.log("You are a teenager.");
} else if (age < 60) {
    console.log("You are an adult."); // Output: You are an adult.
} else {
    console.log("You are a senior citizen.");
}