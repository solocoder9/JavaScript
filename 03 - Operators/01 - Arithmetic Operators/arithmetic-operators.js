// Declaring two variables for arithmetic operations
let num1 = 10;
let num2 = 5;

// Performing addition
let sum = num1 + num2; 
console.log("Addition:", sum); // Output: 15

// Performing subtraction
let difference = num1 - num2;
console.log("Subtraction:", difference); // Output: 5

// Performing multiplication
let product = num1 * num2;
console.log("Multiplication:", product); // Output: 50

// Performing division
let quotient = num1 / num2;
console.log("Division:", quotient); // Output: 2

// Performing modulus (remainder)
let remainder = num1 % num2;
console.log("Modulus:", remainder); // Output: 0

// Performing exponentiation
let power = num1 ** 2;
console.log("Exponentiation:", power); // Output: 100
