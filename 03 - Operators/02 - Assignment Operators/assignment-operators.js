// Declaring a variable and assigning an initial value
let num = 10;
console.log("Initial Value:", num); // Output: 10

// Using addition assignment (+=) - adds and assigns the value
num += 5; // Equivalent to: num = num + 5
console.log("After += 5:", num); // Output: 15

// Using subtraction assignment (-=) - subtracts and assigns the value
num -= 3; // Equivalent to: num = num - 3
console.log("After -= 3:", num); // Output: 12

// Using multiplication assignment (*=) - multiplies and assigns the value
num *= 2; // Equivalent to: num = num * 2
console.log("After *= 2:", num); // Output: 24

// Using division assignment (/=) - divides and assigns the value
num /= 4; // Equivalent to: num = num / 4
console.log("After /= 4:", num); // Output: 6

// Using modulus assignment (%=) - assigns the remainder
num %= 5; // Equivalent to: num = num % 5
console.log("After %= 5:", num); // Output: 1
